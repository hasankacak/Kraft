package day_29;

public class WrapperClass_2 {
    public static void main(String[] args) {
        Integer I= 10;
        int i = I.intValue();
        System.out.println("i = " +i);

        Byte B=100;
        byte b=B.byteValue();
        System.out.println("b = " + b);

        Boolean Bl= true;
        boolean bl=Bl.booleanValue();
        System.out.println("bl = " + bl);

    }
}

